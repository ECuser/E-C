def get_conversion_map(uploaded_df=None):
    """
    Return the PC-per-BOX mapping.
    If uploaded_df is provided (a DataFrame with the SAP product list),
    build the map from its column A (Product) and column E (PC per BOX),
    overriding the hardcoded defaults.
    """
    if uploaded_df is None:
        return dict(PC_PER_BOX)

    mapping = {}
    cols = list(uploaded_df.columns)
    # Column A = product id (index 0), Column E = PC per BOX (index 4)
    if len(cols) < 5:
        return dict(PC_PER_BOX)
    prod_col = cols[0]
    pcbox_col = cols[4]
    for _, row in uploaded_df.iterrows():
        product = row[prod_col]
        pc_per_box = row[pcbox_col]
        if product is None or str(product).strip() == "":
            continue
        try:
            val = int(float(pc_per_box))
        except (ValueError, TypeError):
            continue
        pid = str(product).strip()
        if pid.endswith(".0"):
            pid = pid[:-2]
        mapping[pid] = val
    return mapping if mapping else dict(PC_PER_BOX)
